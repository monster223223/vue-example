import { mapActions, mapGetters } from 'vuex'

export default {
  name: 'Helloworld',
  data() {
    return {
      spellName: null
    }
  },
  computed: {
    ...mapGetters([
      'spells',
      'singleSpell',
      'base',
      'key'
    ]),
  },
  mounted() {
    this.fetchSpells()
  },
  methods: {
    ...mapActions([
      'fillSpells',
      'fillSingleSpell'
    ]),
    fetchSpells() {
      const base = this.base
      const key = this.key
      fetch(`${base}/spells?key=${key}`)
      .then(resp => resp.json())
      .then(obj => {
        this.fillSpells(obj)
      })
    },
    search() {
      const singleSpell = this.spells.filter(spell => spell.spell.toLowerCase().includes(this.spellName.toLowerCase()))
      this.fillSingleSpell(singleSpell)
    }
  }
}
